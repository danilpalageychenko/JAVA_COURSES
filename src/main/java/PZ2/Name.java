public class Name {
    int rate;
    String man;
    String woman;

    public Name(int rate, String man, String woman) {
        this.rate = rate;
        this.man = man;
        this.woman = woman;
    }

    public int getRate() {
        return rate;
    }

    public String getMan() {
        return man;
    }

    public String getWoman() {
        return woman;
    }
}
